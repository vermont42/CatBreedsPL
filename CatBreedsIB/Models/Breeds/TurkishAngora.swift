The Turkish Angora is a breed of domestic cat. Turkish Angoras are one of the ancient, natural breeds of cat, having originated in central Turkey. The breed has been documented as early as the 17th century and is believed to be the origin of the mutations for both the coloration white and long hair. The breed is also sometimes referred to as simply the Angora or Ankara cat.

Like all domestic cats, Turkish Angoras descended from the African wildcat (Felis silvestris lybica). The Fertile Crescent was a place where cats were first domesticated. Cats from eastern mountainous regions of Anatolia developed into longhaired breeds like the Turkish Van and the Turkish Angora through inbreeding and natural selection.

Turkish Angora cats have long, silky coats and elegant, sinuous bodies. Though it is known for a shimmery white coat, Turkish angora cats can display a variety of colors. They come in tabby and tabby-white, along with black with an undercoat of chocolate brown, and lastly smoke varieties, and are in every color other than those that indicate crossbreeding, such as pointed, chocolate and lavender.

Eyes may be blue, green, amber, yellow, or heterochromatic (e.g., one blue and one amber or green). Ears are pointed, large and wide-set. The eyes are almond shaped and the profile forms two straight planes. The plumed tail is often carried upright, perpendicular to the back.
