  var fullDescription: String {
    return """
The Russian Blue is a cat breed that comes in colors varying from a light shimmering silver to a darker, slate grey. They develop close bonds with their owners and are sought out as pets due to their personalities, beauty and coat. It is their short, dense coat which has been the hallmark of the Russian breed for more than a century. The dense coat stands out from the body and one can draw patterns in the coat that will stay until one smoothes them out again. They are also considered to be hypoallergenic.

The Russian Blue is a naturally occurring breed that may have originated in the port of Arkhangelsk, Russia. They are also sometimes called Archangel Blues. It is believed that sailors took Russian Blues from the Archangel Isles to Great Britain and Northern Europe in the 1860s. The first recorded appearance outside of Russia was in 1875 at the Crystal Palace in England, as the Archangel Cat. The Russian Blue competed in a class including all other blue cats until 1912, when it was given its own class.

The Russian Blue has bright green eyes, pinkish lavender or mauve paws, two layers of short thick fur, and a blue-grey coat. The color is a bluish-gray that is the dilute expression of the black gene. However, as dilute genes are recessive ("d") and each parent will have a set of two recessive genes ("dd") two non-CPC Russian Blues will always produce a blue cat. Due to the breeding with Siamese after WWII there are colorpoint genes floating around. If two carriers are bred together then they will produce a litter of mixed colors – solid blue or white with blue like a Siamese. People call these CPC cats "colorpoint", "whites" or "pointed" Russians. In most registries one cannot register, breed or show a colorpoint Russian.
"""
